function generateAvailability(days: number = 30, seed: number = 0): Availability[] {
  const result: Availability[] = [];
  const now = new Date();
  for (let i = 0; i < days; i++) {
    const date = new Date(now);
    date.setDate(date.getDate() + i);
    const pseudoRandom = ((i + seed) * 7 + 3) % 10;
    result.push({
      date: date.toISOString().split('T')[0],
      available: pseudoRandom > 1,
      capacityLeft: pseudoRandom > 1 ? ((pseudoRandom * 2) % 5) + 1 : 0,
    });
  }
  return result;
}

